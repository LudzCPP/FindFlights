const generatePriceHistory = (
  routeKey: string,
  origin: string,
  destination: string,
  basePrice: number,
  currentPrice: number,
): PriceHistory => {
  const now = new Date()
  const dataPoints = Array.from({ length: 26 }, (_, i) => {
    const date = new Date(now)
    date.setDate(date.getDate() - (25 - i) * 7)
    const variance = (Math.random() - 0.4) * basePrice * 0.4
    const trend = i < 10 ? basePrice * 1.1 : i < 18 ? basePrice * 0.95 : basePrice * 0.85
    const price = Math.max(99, Math.round(trend + variance))
    return {
      date: date.toISOString().split('T')[0],
      price,
    }
  })
  dataPoints[dataPoints.length - 1].price = currentPrice

  const prices = dataPoints.map(d => d.price)
  return {
    routeKey,
    origin,
    destination,
    dataPoints,
    averagePrice: Math.round(prices.reduce((a, b) => a + b, 0) / prices.length),
    lowestPrice: Math.min(...prices),
    highestPrice: Math.max(...prices),
    currentPrice,
  }
}

